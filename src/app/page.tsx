// Small, reusable demo card with glassmorphism + subtle gradient accent
function DemoCard({ title, children, accent = "from-indigo-500 to-pink-500" }) {
  return (
    <motion.article
      layout
      initial={{ opacity: 0, y: 12 }}
      animate={{ opacity: 1, y: 0 }}
      exit={{ opacity: 0, y: -8 }}
      whileHover={{ scale: 1.02 }}
      transition={{ duration: 0.28 }}
      className="relative rounded-2xl p-5 shadow-lg bg-white/70 dark:bg-slate-800/60 backdrop-blur-sm border border-gray-100 dark:border-slate-700"
    >
      <div className={`inline-flex items-center gap-3 mb-4 px-3 py-1 rounded-full text-white text-sm bg-gradient-to-r ${accent} shadow-sm`}>
        <Star className="w-4 h-4 opacity-90" />
        <span className="font-semibold">{title}</span>
      </div>

      <div className="min-h-[64px] flex items-center justify-center">{children}</div>

      <div className="mt-4 flex items-center justify-between">
        <div className="flex gap-2 flex-wrap">
          <span className="text-xs bg-slate-50 dark:bg-slate-700 px-2 py-1 rounded-full">Interactive</span>
          <span className="text-xs bg-slate-50 dark:bg-slate-700 px-2 py-1 rounded-full">Demo</span>
        </div>
        <button className="inline-flex items-center gap-2 text-sm font-medium text-indigo-600 hover:text-indigo-700">
          Try <ArrowRight className="w-4 h-4" />
        </button>
      </div>
    </motion.article>
  );
}

